type TranslationValue = string | ((params?: Record<string, string | number>) => string)

const formatTemplate = (template: string, params?: Record<string, string | number>) => {
  if (!params) {
    return template
  }

  return template.replace(/\{(\w+)\}/g, (_match, key) => String(params[key] ?? ''))
}

