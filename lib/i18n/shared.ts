export type I18nKey = keyof typeof zhCN

export const supportedAppLanguages: AppLanguage[] = ['zh-CN', 'en']

export const resolveAppLanguage = (language?: string | null): AppLanguage => {
  if (language === 'en') {
    return 'en'
  }

  return 'zh-CN'
}

export const translateMessage = (language: AppLanguage, key: I18nKey, params?: Record<string, string | number>) => {
  const value = messages[language][key] ?? messages['zh-CN'][key]

  if (typeof value === 'function') {
    return value(params)
  }

  return formatTemplate(value, params)
}

export const formatLanguageLabel = (language: AppLanguage) => {
  return translateMessage(language, `app.language.${language}` as I18nKey)
}
